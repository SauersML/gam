#[test]
fn fit_location_scale_survival_with_constant_noise_on_small_right_censored_data() {
    init_parallelism();
    let data = small_right_censored_location_scale_dataset();
    let config = FitConfig {
        survival_likelihood: "location-scale".to_string(),
        noise_formula: Some("1".to_string()),
        ..FitConfig::default()
    };

    let result = fit_from_formula("Surv(entry, exit, event) ~ x", &data, &config)
        .expect("small right-censored location-scale fit should succeed");
    let FitResult::SurvivalLocationScale(fit) = result else {
        panic!("expected survival location-scale fit result");
    };
    let unified = &fit.fit.fit;
    let beta_time = unified.beta_time();
    let beta_threshold = unified.beta_threshold();
    let beta_log_sigma = unified.beta_log_sigma();
    assert!(
        unified.outer_converged,
        "outer optimizer did not converge: iterations={} grad_norm={:.3e} lambdas={:?} log_lambdas={:?} pirls_status={:?} beta_time={:?} beta_threshold={:?} beta_log_sigma={:?}",
        unified.outer_iterations,
        unified.outer_gradient_norm,
        unified.lambdas.to_vec(),
        unified.log_lambdas.to_vec(),
        unified.pirls_status,
        beta_time.to_vec(),
        beta_threshold.to_vec(),
        beta_log_sigma.to_vec()
    );
    assert_eq!(unified.pirls_status, PirlsStatus::Converged);
    assert!(
        beta_time
            .iter()
            .chain(beta_threshold.iter())
            .chain(beta_log_sigma.iter())
            .all(|value| value.is_finite())
    );
}
